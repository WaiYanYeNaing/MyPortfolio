export const calculateSizes = (isSmall, isMobile, isTablet) => {
  return {
    deskScale: isSmall ? 0.06 : isMobile ? 0.09 : 0.09,
    deskPosition: isMobile ? [-3, -6, -10] : [0, -5, -10],
    cubePosition: isSmall
      ? [4, -5, 0]
      : isMobile
      ? [5, -5, 0]
      : isTablet
      ? [10, -9, 0]
      : [15, -6.5, 0],
    reactLogoPosition: isSmall
      ? [3, 4, 0]
      : isMobile
      ? [6, 4, 0]
      : isTablet
      ? [8, 4, 0]
      : [12, 3, 0],
    ringPosition: isSmall
      ? [-5, 7, 0]
      : isMobile
      ? [-15, 10, 0]
      : isTablet
      ? [-15, 10, 0]
      : [-24, 10, 0],
    targetPosition: isSmall
      ? [-5, -10, -10]
      : isMobile
      ? [-9, -10, -10]
      : isTablet
      ? [-11, -9, -10]
      : [-13, -8, -10],
  };
};

